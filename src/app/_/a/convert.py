def replace_symbols(file_path):
    with open(file_path, 'r') as file:
        content = file.read()

    for symbol, jsx_repr in symbol_mapping.items():
        content = re.sub(re.escape(symbol), jsx_repr, content)

    with open(file_path, 'w') as file:
        file.write(content)

if __name__ == '__main__':
    if len(sys.argv) < 2:
        print("Usage: python script.py <file_path>")
        sys.exit(1)

    markdown_file_path = sys.argv[1]
    replace_symbols(markdown_file_path)
